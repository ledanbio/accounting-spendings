#!/bin/bash

set -e

echo "================================"
echo "🔄 Запуск процесса обновления"
echo "================================"
echo ""

# Цвета для вывода
RED='\033[0;31m'
GREEN='\033[0;32m'
YELLOW='\033[1;33m'
NC='\033[0m' # No Color

# Функция для вывода ошибок
error_exit() {
    echo -e "${RED}❌ Ошибка: $1${NC}"
    exit 1
}

# Функция для вывода успеха
success() {
    echo -e "${GREEN}✅ $1${NC}"
}

# Функция для информационного сообщения
info() {
    echo -e "${YELLOW}ℹ️  $1${NC}"
}

# Проверка что мы в правильной директории
if [ ! -f "Makefile" ] || [ ! -f ".env" ]; then
    error_exit "Не найдены файлы Makefile или .env. Убедитесь что вы в правильной директории."
fi

# Шаг 1: Остановка контейнеров
info "Остановка контейнеров..."
make down || error_exit "Не удалось остановить контейнеры"
success "Контейнеры остановлены"
echo ""

# Шаг 2: Git pull
info "Получение обновлений из репозитория..."
git pull || error_exit "Не удалось выполнить git pull"
success "Обновления получены"
echo ""

# Шаг 3: Сборка новой версии
info "Сборка новой версии бота (без кэша)..."
docker compose --env-file .env -f deploy/docker-compose.yml build --no-cache bot || error_exit "Не удалось собрать образ"
success "Образ собран"
echo ""

# Шаг 4: Запуск контейнеров
info "Запуск контейнеров..."
make up || error_exit "Не удалось запустить контейнеры"
success "Контейнеры запущены"
echo ""

# Финальное сообщение
echo "================================"
echo -e "${GREEN}🎉 Обновление завершено успешно!${NC}"
echo "================================"
echo ""
echo "Логи контейнера:"
echo "  make logs      - просмотр логов в реальном времени"
echo "  make logs-tail - последние 200 строк логов"
echo ""
echo "Управление:"
echo "  make down      - остановить контейнеры"
echo "  make up        - запустить контейнеры"
echo "  make db-shell  - подключиться к БД"
